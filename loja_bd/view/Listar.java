    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        if (lista.getSelectedIndex() > -1) {
            for (produto p : controller.listarProdutos()) {
                if (p.getId() == lista.getSelectedIndex()) {
                    System.out.println("Nome: " + p.getNome());
                }
            }
        }
    }//GEN-LAST:event_listaValueChanged

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selectedIndex = lista.getSelectedIndex();
        if (selectedIndex != -1) {
            produto produto = produtos.get(selectedIndex);
            int confirm = JOptionPane.showConfirmDialog(null, "Tem certeza?", "Exlcuir", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, "Produto Exclu√≠do!");
                controller.deletarProduto(produto.getId());
                atualizarLista();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        atualizar telaAtualizar = new atualizar();
        telaAtualizar.setVisible(true);
        int selectedIndex = lista.getSelectedIndex();
        if (selectedIndex != -1) { // Verifica se algo foi selecionado
            produto produtoSelecionado = produtos.get(selectedIndex);

            // Percorre a lista de produtos para encontrar o correto
            for (produto prod : controller.listarProdutos()) {
                if (prod.getId() == produtoSelecionado.getId()) { 
                    // Preenche os campos de texto com os atributos do produto
                    telaAtualizar.getTxtId().setText(String.valueOf(prod.getId()));
                    telaAtualizar.getTxtNome().setText(prod.getNome());
                    telaAtualizar.getTxtDescricao().setText(prod.getDescricao());
                    telaAtualizar.getTxtQuantidade().setText(String.valueOf(prod.getQuantidade())); // int para String
                    telaAtualizar.getTxtPreco().setText(String.valueOf(prod.getPreco())); // double para String

                    break; // Produto encontrado, sai do loop
                }
            }
        }
        this.dispose();
    }//GEN-LAST:event_btnEditarActionPerformed

