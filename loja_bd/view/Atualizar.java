    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        String nomeNovo = nome.getText();
        String descricaoNova = descricao.getText();
        String quantidadeNova = quantidade.getText();
        String precoNovo = preco.getText();
        int quantidade = Integer.parseInt(quantidadeNova);
        double preco = Double.parseDouble(precoNovo);
        String idTexto = id.getText();
        int id = Integer.parseInt(idTexto);

        if (controller.atualizarProdutos(id, nomeNovo, descricaoNova, quantidade, preco)) {
            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
            new TelaPrincipal().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "NÃ£o foi possivel atualizar.");//(parentCongenems this, message "Login bem-sucedidol*);
        }
    }//GEN-LAST:event_editarActionPerformed

